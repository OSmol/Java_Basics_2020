public class SecondTask3 {
	/*
	 * Необходимо вывести на экран таблицу умножения на 3. 
	 */
			

	public static void main(String[] args) {
		int number=-1;
	
		System.out.println("Таблица умножение на 3: ");
		while (number<10)
			{	
		       number=number+1;
		       System.out.println(number+"*3="+number*3);			       
	         }
       }

}
